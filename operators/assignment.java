package operators;

public class assignment {
    public static void main(String[] args) {
        int a = 10;
        // a = a + 1
        a += 1;
        System.out.println(a);

        int b = 30;
        // b = b - 1
        b -= 1;
        System.out.println(b);

        int c = 5;
        // c = c * 5
        c *= 3;
        System.out.println(c);

        int d = 99;
        // d = d/11
        d /= 11;
        System.out.println(d);

        int e = 10;
        // e = e % 5
        e %= 5;
        System.out.println(e);
        
    }
    
}
